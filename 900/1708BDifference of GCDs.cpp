void solve()
{
    srand(time(0));
    int n, l, r, flag = 0;
    cin >> n >> l >> r;
    int Arr[n];
    for (int i = 1; i <= n; i++)
    {
        Arr[i - 1] = (((l - 1) / i) + 1) * i;
        if (Arr[i - 1] > r)
        {
            cout << "NO\n";
            return;
        }
    }
    cout << "YES\n";
    for (int i = 0; i < n; i++)
    {
        cout << Arr[i] << " ";
    }
    cout << "\n";
}
