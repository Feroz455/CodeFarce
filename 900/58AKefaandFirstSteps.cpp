int val[10000000];
int main()
{
    int n, check1 = 1, check2 = 0;
    cin >> n;
    cin >> val[0];
    for (int i = 1; i < n; i++)
    {
        cin >> val[i];
        if (val[i - 1] <= val[i])
            check1++;
        else
        {

            if (check2 <= check1)
            {
                check2 = check1;
            }
            check1 = 1;
        }
    }
    if (check1 <= check2)
        cout << check2;
    else
        cout << check1;
    return 0;
}
